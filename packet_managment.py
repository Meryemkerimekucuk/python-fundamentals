# Conda sanal ortam ve paket yönetimi
# pip paket yönetimi

# Sanal ortamı görüntüleyebilmek için: conda env list
# Yeni bir sanal ortam oluşturmak için: conda create -n mylist
# Sanal ortam active edebilmek için: conda activate mylist
# Sanal ortamı deactive edebilmek için: conda deactivate mylist
# Sanal ortamda bütün listeleri görebilmek için: conda list
# Paket yüklemek için: conda install numpy
# Aynı anda birden fazla paket yüklemek için: conda install pandas matplotlib
# Paket silmek için: conda remove numpy
# Belirli bir versiyona göre paket yükleme: conda install numpy= 1.20.1
# Upgrade işlemi için: conda upgrade conda
# Paket yüklemek için: pip install pandas
# Versiyona göre paket yüklemek için: pip install numpy==1.20.1
# Paketleri bir dosyaya aktarmak için: conda env export > environment.yaml
# pycharm da çalışma dizinindeki listeleri görüntüleyebilmek için: ls
# silinen bir sanal ortamdaki paketleri getirebilmek için : conda env create -f environment.yaml